class CWoodTexture : public rtTexture::rtCParametricTexture
{
	rtTexture::RT_COLORS_MAP_ELEMENT* m_colorMap;
	int                                         m_colorMap_size;
	RT_VECTOR  m_turbulence;
	RT_VECTOR  m_scale;
	int        m_steps_count;
	double     m_random_shift;
	double     m_step_coef;
public:
	CWoodTexture(rtTexture::RT_COLORS_MAP_ELEMENT* cMap,
					int cMapSize,
					double turb_x, double turb_y, double turb_z,
				double scale_x, double scale_y, double scale_z,
				int steps_count, double random_shift, double step_coef)
	{
		m_colorMap = cMap;
		m_colorMap_size = cMapSize;
		m_turbulence.x = turb_x;
		m_turbulence.y = turb_y;
		m_turbulence.z = turb_z;

		m_scale.x = scale_x;
		m_scale.y = scale_y;
		m_scale.z = scale_z;

		m_steps_count = steps_count;
		m_random_shift = random_shift;
		m_step_coef = step_coef;
	}

	virtual  rtTexture::RT_PARAMETRIC_TEXTURE_TYPE    GetParametricType() const
	{	return rtTexture::RTPT_WOOD;	}

	virtual  int                           GetColorsMapSize() const
	{
		return m_colorMap_size;
	}
	virtual  const rtTexture::RT_COLORS_MAP_ELEMENT*  GetColorsMap() const
	{
		return m_colorMap;
	}
	
	virtual  void                          GetColorsMapTurbulenceParams(RT_VECTOR& turbVec,
																				int&   stepsCountParam, 
																				double& randomShiftParam,
																				double& stepCoeffParam) const
	{
		turbVec = m_turbulence;
		stepsCountParam = m_steps_count;
		randomShiftParam = m_random_shift;
		stepCoeffParam = m_step_coef;
	};
	virtual  RT_VECTOR                   GetScaleVector() const
	{   
		return m_scale;
	};
};


