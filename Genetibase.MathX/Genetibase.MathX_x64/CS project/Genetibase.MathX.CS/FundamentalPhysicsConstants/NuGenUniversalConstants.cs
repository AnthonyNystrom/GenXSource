
using System;

namespace Genetibase.MathX.Constants
{

	/// <summary>
	/// Universal Constants
	/// </summary>
	public class  NuGenUniversalConstants 
	{

		private NuGenUniversalConstants()
		{
			
		}

		/// <summary>
		/// speed of light in vacuum
		/// </summary>
		public const double SpeedOfLightInVacuum = 299792458; //speed of light in vacuum

		/// <summary>
		/// magn. constant
		/// </summary>
		public const double MagnConstant = 12.566370614e-7; //magn. constant

		/// <summary>
		/// electric constant
		/// </summary>
		public const double ElectricConstant = 8.854187817e-12; //electric constant

		/// <summary>
		/// characteristic impedance of vacuum
		/// </summary>
		public const double CharacteristicImpedanceOfVacuum = 376.730313461; //characteristic impedance of vacuum

		/// <summary>
		/// Newtonian constant of gravitation
		/// </summary>
		public const double NewtonianConstantOfGravitation = 0.000000000066742; //Newtonian constant of gravitation

		/// <summary>
		/// Newtonian constant of gravitation over h-bar c
		/// </summary>
		public const double NewtonianConstantOfGravitationOverHBarC = 6.7087E-39; //Newtonian constant of gravitation over h-bar c

		/// <summary>
		/// Planck constant
		/// </summary>
		public const double PlanckConstant = 6.6260693e-34; //Planck constant

		/// <summary>
		/// Planck constant in eV s
		/// </summary>
		public const double PlanckConstantInEvS = 4.13566743e-15; //Planck constant in eV s

		/// <summary>
		/// Planck constant over 2 pi times c in MeV fm
		/// </summary>
		public const double PlanckConstantOver2PiTimesCInMevFm = 197.326968; //Planck constant over 2 pi times c in MeV fm

		/// <summary>
		/// Planck constant over 2 pi
		/// </summary>
		public const double PlanckConstantOver2Pi = 1.05457168e-34; //Planck constant over 2 pi

		/// <summary>
		/// Planck constant over 2 pi in eV s
		/// </summary>
		public const double PlanckConstantOver2PiInEvS = 6.58211915e-16; //Planck constant over 2 pi in eV s

		/// <summary>
		/// Planck mass
		/// </summary>
		public const double PlanckMass = 2.17645e-8; //Planck mass

		/// <summary>
		/// Planck temperature
		/// </summary>
		public const double PlanckTemperature = 1.41679e32; //Planck temperature

		/// <summary>
		/// Planck length
		/// </summary>
		public const double PlanckLength = 1.61624e-35; //Planck length

		/// <summary>
		/// Planck time
		/// </summary>
		public const double PlanckTime = 5.39121e-44; //Planck time
	}
}
